import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { execSync } from 'node:child_process';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const repoRoot = path.resolve(__dirname, '..');
const escrowArtifactPath = path.join(repoRoot, 'contracts/artifacts/contracts/HTLCEscrow.sol/HTLCEscrow.json');
const registryArtifactPath = path.join(repoRoot, 'contracts/artifacts/contracts/ResolverRegistry.sol/ResolverRegistry.json');
const sdkAbiPath = path.join(repoRoot, 'packages/sdk/src/ethereum/abi.ts');

function main() {
  console.log('Validating contract artifacts and SDK ABI...');
  let failed = false;

  // 1. Verify artifacts exist
  const expectedArtifacts = [
    { name: 'HTLCEscrow', path: escrowArtifactPath },
    { name: 'ResolverRegistry', path: registryArtifactPath }
  ];

  for (const artifact of expectedArtifacts) {
    if (!fs.existsSync(artifact.path)) {
      console.error(`❌ Error: Missing contract artifact for ${artifact.name} at:`);
      console.error(`   ${artifact.path}`);
      failed = true;
    } else {
      console.log(`✓ Found artifact: ${artifact.name}`);
    }
  }

  if (failed) {
    console.error('Please run "pnpm --filter @wafflefinance/contracts compile" to generate artifacts.');
    process.exit(1);
  }

  // 2. Validate SDK ABI matches compiled HTLCEscrow ABI
  try {
    const artifact = JSON.parse(fs.readFileSync(escrowArtifactPath, 'utf8'));
    const abi = artifact.abi;
    const expectedContent = `// This file is auto-generated from the compiled HTLCEscrow contract artifact.
// Do not edit this file manually.

export const HTLC_ESCROW_ABI = ${JSON.stringify(abi, null, 2)} as const;
`;

    if (!fs.existsSync(sdkAbiPath)) {
      console.error(`❌ Error: SDK ABI file not found at: ${sdkAbiPath}`);
      failed = true;
    } else {
      const actualContent = fs.readFileSync(sdkAbiPath, 'utf8');
      if (actualContent.trim() !== expectedContent.trim()) {
        console.error('❌ Error: SDK ABI is out of sync with contract compilation output!');
        console.error(`   Expected file content does not match: ${sdkAbiPath}`);
        console.error('   Please run "pnpm --filter @wafflefinance/contracts compile" to synchronize it.');
        failed = true;
      } else {
        console.log('✓ SDK ABI is in sync with contract artifacts.');
      }
    }
  } catch (error) {
    console.error(`❌ Error reading or parsing ABI: ${error.message}`);
    failed = true;
  }

  if (failed) {
    process.exit(1);
  }

  // 3. Git integration checks (only if inside a git repository)
  let isGit = false;
  try {
    execSync('git rev-parse --is-inside-work-tree', { stdio: 'ignore', cwd: repoRoot });
    isGit = true;
  } catch {
    console.log('⚠️  Not inside a git repository. Skipping git status/diff validation.');
  }

  if (isGit) {
    const filesToTrack = [
      escrowArtifactPath,
      registryArtifactPath,
      sdkAbiPath
    ].map(p => path.relative(repoRoot, p));

    // Verify files are tracked
    for (const file of filesToTrack) {
      try {
        execSync(`git ls-files --error-unmatch "${file}"`, { stdio: 'ignore', cwd: repoRoot });
        console.log(`✓ File is tracked by git: ${file}`);
      } catch {
        console.error(`❌ Error: File is NOT tracked by git: ${file}`);
        console.error(`   Please run "git add ${file}" to track it.`);
        failed = true;
      }
    }

    if (failed) {
      process.exit(1);
    }

    // Verify no unstaged/committed drift exists
    try {
      execSync(`git diff --exit-code ${filesToTrack.map(f => `"${f}"`).join(' ')}`, { stdio: 'inherit', cwd: repoRoot });
      console.log('✓ No drift or unstaged changes detected in contract artifacts.');
    } catch {
      console.error('\n❌ Error: Drift detected between contract source files and committed artifacts!');
      console.error('   The committed artifacts or generated SDK ABI do not match the compiled output.');
      console.error('   Please run "pnpm --filter @wafflefinance/contracts compile" and commit the changes.');
      process.exit(1);
    }
  }

  console.log('\n✅ Contract artifacts validation completed successfully!');
}

main();
